// Helper function to get trains by destination
export const getTrainsByDestination = (destination: string) => {
  return trainsToJharkhand.filter(train => 
    train.arrival.toLowerCase().includes(destination.toLowerCase())
  );
};

// Helper function to get trains by source
export const getTrainsBySource = (source: string) => {
  return trainsToJharkhand.filter(train => 
    train.departure.toLowerCase().includes(source.toLowerCase())
  );
};

// Helper function to search trains
export const searchTrains = (searchTerm: string) => {
  const term = searchTerm.toLowerCase();
  return trainsToJharkhand.filter(train => 
    train.trainName.toLowerCase().includes(term) ||
    train.trainNumber.includes(term) ||
    train.departure.toLowerCase().includes(term) ||
    train.arrival.toLowerCase().includes(term) ||
    train.route.some(station => station.toLowerCase().includes(term))
  );
};

// Helper function to get featured trains
export const getFeaturedTrains = () => {
  return trainsToJharkhand.filter(train => train.isFeatured);
};
